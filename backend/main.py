# helper functions
def haversine(lat1, lon1, lat2, lon2): # with the power of google and searching up how to filter our locations we got this
    earthRadius = 6371000
    
    phi1 = math.radians(lat1)
    phi2 = math.radians(lat2)
    
    deltaPhi = math.radians(lat2 - lat1)
    deltaLambda = math.radians(lon2 - lon1)
    
    a = math.sin(deltaPhi / 2) ** 2 + math.cos(phi1) * math.cos(phi2) * math.sin(deltaLambda / 2) ** 2
    
    return earthRadius * 2 * math.atan2(math.sqrt(a), math.sqrt(1 - a)) # the shortest distance between two points on earth 

